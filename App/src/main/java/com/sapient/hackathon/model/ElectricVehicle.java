package com.sapient.hackathon.model;

public class ElectricVehicle {
	
	private int percentageEV;
	private int requiredMW;
	private String startHour;
	
	
	public int getPercentageEV() {
		return percentageEV;
	}
	public void setPercentageEV(int percentageEV) {
		this.percentageEV = percentageEV;
	}
	public int getRequiredMW() {
		return requiredMW;
	}
	public void setRequiredMW(int requiredMW) {
		this.requiredMW = requiredMW;
	}
	public String getStartHour() {
		return startHour;
	}
	public void setStartHour(String startHour) {
		this.startHour = startHour;
	}
	
	
}
